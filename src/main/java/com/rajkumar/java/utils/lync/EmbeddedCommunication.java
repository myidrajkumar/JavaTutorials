package com.rajkumar.java.utils.lync;

public class EmbeddedCommunication {
  
  private String embeddedCommunicationSupportedModalities;
  private String embeddedCommunicationRel;
  private String embeddedCommunicationETag;
  private String embeddedCommunicationSupportedMessageFormat;
  private EmbeddedCommunicationLink embeddedCommunicationLink;
  
  public String getEmbeddedCommunicationSupportedMessageFormat() {
    
    return embeddedCommunicationSupportedMessageFormat;
  }
  
  public void setEmbeddedCommunicationSupportedMessageFormat(
      String embeddedCommunicationSupportedMessageFormat) {
    
    this.embeddedCommunicationSupportedMessageFormat = embeddedCommunicationSupportedMessageFormat;
  }
  
  public EmbeddedCommunicationLink getEmbeddedCommunicationLink() {
    
    return embeddedCommunicationLink;
  }
  
  public void setEmbeddedCommunicationLink(
      EmbeddedCommunicationLink embeddedCommunicationLink) {
    
    this.embeddedCommunicationLink = embeddedCommunicationLink;
  }
  
  public String getEmbeddedCommunicationSupportedModalities() {
    
    return embeddedCommunicationSupportedModalities;
  }
  
  public void setEmbeddedCommunicationSupportedModalities(
      String embeddedCommunicationSupportedModalities) {
    
    this.embeddedCommunicationSupportedModalities = embeddedCommunicationSupportedModalities;
  }
  
  public String getEmbeddedCommunicationRel() {
    
    return embeddedCommunicationRel;
  }
  
  public void setEmbeddedCommunicationRel(String embeddedCommunicationRel) {
    
    this.embeddedCommunicationRel = embeddedCommunicationRel;
  }
  
  public String getEmbeddedCommunicationETag() {
    
    return embeddedCommunicationETag;
  }
  
  public void setEmbeddedCommunicationETag(String embeddedCommunicationETag) {
    
    this.embeddedCommunicationETag = embeddedCommunicationETag;
  }
  
}
