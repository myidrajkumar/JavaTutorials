package com.rajkumar.java.utils.lync;

public class EmbeddedMe {
	
	private String embeddedMeName;
	private String embeddedMeURI;
	private String embeddedMeEMailAddresses;
	private String embeddedMeTitle;
	private String embeddedMeDepartment;
	
	EmbeddedMeLink embeddedMeLink;
	
	String embeddedMeRel;
	
	public String getEmbeddedMeName() {
		return embeddedMeName;
	}
	public void setEmbeddedMeName(String embeddedMeName) {
		this.embeddedMeName = embeddedMeName;
	}
	public String getEmbeddedMeURI() {
		return embeddedMeURI;
	}
	public void setEmbeddedMeURI(String embeddedMeURI) {
		this.embeddedMeURI = embeddedMeURI;
	}
	public String getEmbeddedMeEMailAddresses() {
		return embeddedMeEMailAddresses;
	}
	public void setEmbeddedMeEMailAddresses(String embeddedMeEMailAddresses) {
		this.embeddedMeEMailAddresses = embeddedMeEMailAddresses;
	}
	public String getEmbeddedMeTitle() {
		return embeddedMeTitle;
	}
	public void setEmbeddedMeTitle(String embeddedMeTitle) {
		this.embeddedMeTitle = embeddedMeTitle;
	}
	public String getEmbeddedMeDepartment() {
		return embeddedMeDepartment;
	}
	public void setEmbeddedMeDepartment(String embeddedMeDepartment) {
		this.embeddedMeDepartment = embeddedMeDepartment;
	}
	
	public String getEmbeddedMeRel() {
		return embeddedMeRel;
	}
	public void setEmbeddedMeRel(String embeddedMeRel) {
		this.embeddedMeRel = embeddedMeRel;
	}
	
	public EmbeddedMeLink getEmbeddedMeLink() {
		return embeddedMeLink;
	}
	public void setEmbeddedMeLink(EmbeddedMeLink embeddedMeLink) {
		this.embeddedMeLink = embeddedMeLink;
	}

}
