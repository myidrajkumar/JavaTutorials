package com.rajkumar.java.utils.lync;

public class Embedded {
	
	private EmbeddedMe embeddedMe;
	private EmbeddedPeople embeddedPeople;
	private EmbeddedOnlineMeetings embeddedOnlineMeetings;
	private EmbeddedCommunication embeddedCommunication;
	
	public EmbeddedMe getEmbeddedMe() {
		return embeddedMe;
	}
	public void setEmbeddedMe(EmbeddedMe embeddedMe) {
		this.embeddedMe = embeddedMe;
	}
	public EmbeddedPeople getEmbeddedPeople() {
		return embeddedPeople;
	}
	public void setEmbeddedPeople(EmbeddedPeople embeddedPeople) {
		this.embeddedPeople = embeddedPeople;
	}
	public EmbeddedOnlineMeetings getEmbeddedOnlineMeetings() {
		return embeddedOnlineMeetings;
	}
	public void setEmbeddedOnlineMeetings(EmbeddedOnlineMeetings embeddedOnlineMeetings) {
		this.embeddedOnlineMeetings = embeddedOnlineMeetings;
	}
	public EmbeddedCommunication getEmbeddedCommunication() {
		return embeddedCommunication;
	}
	public void setEmbeddedCommunication(EmbeddedCommunication embeddedCommunication) {
		this.embeddedCommunication = embeddedCommunication;
	}
	

}
